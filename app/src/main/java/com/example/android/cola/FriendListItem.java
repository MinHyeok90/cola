public class FriendListItem {
    private String name;
    private String msg;
    private int res;

    public String getName() {
        return name;
    }

    public String getMsg() {
        return msg;
    }

    public int getRes() {
        return res;
    }
    public void setName(String name) {
        this.name = name;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public void setRes(int res) {
        this.res = res;
    }


}
